def r_p_s(data):
    total_score = 0
    round_score = 0
    rounds = data.split('\n')
    for round in rounds:
        if round:
            round_score = 0
            key, value = round.split(' ')
            if value == 'X':
                round_score += 1
                if key == 'A':
                    round_score += 3
                elif key == 'C':
                    round_score += 6
            if value == 'Y':
                round_score += 2
                if key == 'B':
                    round_score += 3
                elif key == 'A':
                    round_score += 6
            if value == 'Z':
                round_score += 3
                if key == 'B':
                    round_score += 6
                elif key == 'C':
                    round_score += 3
            total_score += round_score
    return total_score

